package Pertemuan4;

public class elseifStatement {
    public static void main(String[] args){
        int time = 9;
        if (time < 10){
            System.out.println("Good Morning.");
        }else if (time < 20){
            System.out.println("Good Day.");
        }else {
            System.out.println("Good Evening");
        }
    }
}
